    {
        if (Auth::check()) {
            // The user is logged in...
            $service = new Service;
            $service->name = $request->name;
            $service->description = $request->descriptions;
            $service->price = $request->price;
            $service->id_user = Auth::id();
            $service->save();
            return dd($service->name);
        }
        return route('root');
    }

